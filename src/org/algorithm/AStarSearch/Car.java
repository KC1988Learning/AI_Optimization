package org.algorithm.AStarSearch;

public class Car {

    private float width;
    private float height;

    public Car(float width, float height){
        this.width = width;
        this.height = height;
    }
    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }


}
